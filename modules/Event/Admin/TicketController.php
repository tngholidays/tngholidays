            }
        }
        $data = [
            'row'            => $row,
            // "selected_terms" => $row->terms->pluck('term_id'),
            // 'attributes'     => $this->attributesClass::where('service', 'hotel_room')->get(),
            'enable_multi_lang'=>true,
            'breadcrumbs'    => [
                [
                    'name' => __('Events'),
                    'url'  => 'admin/module/event'
                ],
                [
                    'name' => __('Event: :name',['name'=>$this->currentEvent->title]),
                    'url'  => 'admin/module/event/edit/'.$this->currentEvent->id
                ],
                [
                    'name' => __('All Tickets'),
                    'url'  => 'admin/module/event/ticket/'.$this->currentEvent->id.'/index'
                ],
                [
                    'name' => __('Edit ticket: :name',['name'=>$row->title]),
                    'url'  => 'admin/module/event/room/'.$this->currentEvent->id.'/edit/'.$id
                ],
            ],
            'page_title'=>__("Edit: :name",['name'=>$row->title]),
            'hotel'=>$this->currentEvent
        ];
        return view('Event::admin.ticket.detail', $data);
    }

    public function store( Request $request, $event_id,$id ){
        if(!$this->hasEventPermission($event_id))
        {
            abort(403);
        }
        if($id>0){
            $this->checkPermission('event_update');
            $row = $this->ticktClass::find($id);
            if (empty($row)) {
                return redirect(route('event.admin.index'));
            }

            if($row->create_user != Auth::id() and !$this->hasPermission('event_manage_others'))
            {
                return redirect(route('event.admin.ticket.index'));
            }

            if($row->parent_id != $event_id)
            {
                return redirect(route('event.admin.ticke.index'));
            }
        }else{
            $this->checkPermission('event_create');
            $row = new $this->ticktClass();
            $row->status = "publish";
        }
        $dataKeys = [
            'title',
            'short_desc',
            'content',
            'image_id',
            'gallery',
            'price',
            'sale_price',
            'number',
            'beds',
            'size',
            'adults',
            'children',
            'status',
        ];

        $row->fillByAttr($dataKeys,$request->input());
	    $row->ical_import_url  = $request->ical_import_url;

        if($id<0){
            $row->parent_id = $event_id;
        }

        $res = $row->save();

        if ($res) {
             $this->saveTimeSlots($row,$request);
            if($id > 0 ){
                return redirect()->back()->with('success',  __('Ticket updated') );
            }else{
                return redirect()->back()->with('success',  __('Ticket created') );
            }
        }
    }

    public function saveTerms($row, $request)
    {
        $this->checkPermission('hotel_manage_attributes');
        if (empty($request->input('terms'))) {
            $this->roomTermClass::where('target_id', $row->id)->delete();
        } else {
            $term_ids = $request->input('terms');
            foreach ($term_ids as $term_id) {
                $this->roomTermClass::firstOrCreate([
                    'term_id' => $term_id,
                    'target_id' => $row->id
                ]);
            }
            $this->roomTermClass::where('target_id', $row->id)->whereNotIn('term_id', $term_ids)->delete();
        }
    }
    public function saveTimeSlots($row, $request)
    {

        if (empty($request->input('time_slots'))) {
            $this->timeSlotClass::where('parent_id', $row->id)->delete();
        } else {
            $time_slots = $request->input('time_slots');
            // dd($time_slots);
            $ids = [];
            foreach ($time_slots as $slot) {

                $dataIn = [
                            'adult_price' => $slot['adult_price'],
                            'child_price' => $slot['child_price'],
                            'time' => date('H:i:s',strtotime($slot['time'])),
                            'parent_id' => $row->id
                        ];
                if (isset($slot['id'])) {
                    if (!empty($slot['time'])) {
                        $createId = $this->timeSlotClass::where('id', $slot['id'])->update($dataIn);
                        array_push($ids,$slot['id']);
                    }
                }else{
                    if (!empty($slot['time'])) {
                        $createId = $this->timeSlotClass::create($dataIn);
                        array_push($ids,$createId->id);
                    }
                }
                
            }
            $this->timeSlotClass::where('parent_id', $row->id)->whereNotIn('id', $ids)->forceDelete();
        }
    }

    public function bulkEdit(Request $request)
    {
        $ids = $request->input('ids');
        $action = $request->input('action');
        if (empty($ids) or !is_array($ids)) {
            return redirect()->back()->with('error', __('No items selected!'));
        }
        if (empty($action)) {
            return redirect()->back()->with('error', __('Please select an action!'));
        }
        switch ($action){
            case "delete":
                foreach ($ids as $id) {
                    $query = $this->ticktClass::where("id", $id);
                    if (!$this->hasPermission('hotel_manage_others')) {
                        $query->where("create_user", Auth::id());
                        $this->checkPermission('hotel_delete');
                    }
                    $query->first();
                    if(!empty($query)){
                        $query->delete();
                    }
                }
                return redirect()->back()->with('success', __('Deleted success!'));
                break;
            case "clone":
                $this->checkPermission('hotel_create');
                foreach ($ids as $id) {
                    (new $this->ticktClass())->saveCloneByID($id);
                }
                return redirect()->back()->with('success', __('Clone success!'));
                break;
            default:
                // Change status
                foreach ($ids as $id) {
                    $query = $this->ticktClass::where("id", $id);
                    if (!$this->hasPermission('hotel_manage_others')) {
                        $query->where("create_user", Auth::id());
                        $this->checkPermission('hotel_update');
                    }
                    $query->update(['status' => $action]);
                }
                return redirect()->back()->with('success', __('Update success!'));
                break;
        }
    }
