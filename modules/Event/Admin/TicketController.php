    protected function hasEventPermission($event_id = false){
        if(empty($event_id)) return false;

        $event = $this->eventClass::find($event_id);
        if(empty($event)) return false;

        if(!$this->hasPermission('event_manage_others') and $event->create_user != Auth::id()){
            return false;
        }

        $this->currentEvent = $event;
        return true;
    }
    public function index(Request $request,$event_id)
    {
        $this->checkPermission('event_view');

        if(!$this->hasEventPermission($event_id))
        {
            abort(403);
        }

        $query = $this->ticktClass::query() ;

        $query->orderBy('id', 'desc');
        if (!empty($event_name = $request->input('s'))) {
            $query->where('title', 'LIKE', '%' . $event_name . '%');
            $query->orderBy('title', 'asc');
        }

        $query->where('parent_id',$event_id);
        $data = [
            'rows'               => $query->with(['author'])->paginate(20),
            'breadcrumbs'        => [
                [
                    'name' => __('Events'),
                    'url'  => 'admin/module/event'
                ],
                [
                    'name' => __('Event: :name',['name'=>$this->currentEvent->title]),
                    'url'  => 'admin/module/event/edit/'.$this->currentEvent->id
                ],
                [
                    'name'  => __('Ticket Management'),
                    'class' => 'active'
                ],
            ],
            'page_title'=>__("Ticket Management"),
            'event'=>$this->currentEvent,
            'row'=> new $this->ticktClass(),
            // 'translation'=>new $this->roomTranslationClass(),
            // 'attributes'     => $this->attributesClass::where('service', 'event_room')->get(),
        ];
        return view('Event::admin.ticket.index', $data);
    }

    public function edit(Request $request, $event_id,$id)
    {
        $this->checkPermission('event_update');

        if(!$this->hasEventPermission($event_id))
        {
            abort(403);
        }

